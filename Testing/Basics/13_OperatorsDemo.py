print(x)

x -= 3 #x = x - 3
print(x)

x *= 3 #x = x * 3
print(x)

x /= 3 #x = x / 3
print(x)

x %= 3 #x = x % 3
print(x)

x //= 3 #x = x // 3
print(x)

x **= 3 #x = x ** 3
print(x)

x=2
